# Las listas pertenecen a colecciones en Python
# En otros lenguajes se conocen como vectores o arreglos
# Pueden albergar distintos tipos de datos en ellas.

nombres = ["Natalia", "Osvaldo", "Liliana", "Ariel"]

# Agregamos elementos con append() para mostrar que se pueden almacenar distintos tipos de datos:
nombres.append("Marcelo") # String
nombres.append([1, 2, 3]) # Lista
nombres.append(True) # Booleano
nombres.append(10.45) # Flotante
nombres.append([4, 5])
nombres.append(7)
print(nombres)